# STRING
first_name = "Gabriel"
last_name = 'Gatti'
full_name = first_name + " " + last_name
print(type(full_name))
print("My name is " + full_name)

# INT
age = 21
age += 1
print("My age is " + str(age))
print(type(age))

# FLOAT
height = 180
print("My height is " + str(height) + 'cm')
print(type(height))

# BOOLEAN
human = True
print("I'm a human: " + str(human))
print(type(human))